package data;

public class FloatDoubleType {

	public static void main(String[] args) {

		
		// 실수 자료형
		
		float fNum = 1.23456789F;	// 소수점 이하 6자리까지 출력
		double dNum = 1.23456789012345678;	// 소수점 이하 16자리까지 출력된다.
		
		System.out.println(fNum);	// 1.2345679 
		System.out.println(dNum);	// 1.2345678901234567
	}

}
